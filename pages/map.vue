			e.target.setAttribute('class','selected');
			let activeLi = document.getElementsByClassName('selected');

			for(let j=0; j < activeLi.length;j++ ){
				if(activeLi[j].getAttribute('id').slice(0,-1) == final){
					activeLi[j].setAttribute('class','selected')
				}else{
					activeLi[j].removeAttribute('class')
				}
			}

			const mapSvg = document.getElementById(final);
			mapSvg.setAttribute('class', 'active');
			let activeFlags = document.getElementsByClassName('active');

			for (let i = 0; i < activeFlags.length; i++) {
				if (activeFlags[i].getAttribute('id') == final) {
					activeFlags[i].setAttribute('class', 'active')
				} else {
					activeFlags[i].removeAttribute('class')
				}
			}

		}
	}
}
